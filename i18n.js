export class I18n {
  constructor() {
    this.currentLang = localStorage.getItem('language') || 'en';
    this.fallbackLang = 'en';
    this.listeners = [];
    this.init();
  }

  /**
   * Initialize i18n
   */
  init() {
    // Load saved language
    const savedLang = localStorage.getItem('language');
    if (savedLang && translations[savedLang]) {
      this.currentLang = savedLang;
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = this.currentLang;
    
    console.log(`‚úÖ I18n initialized with language: ${this.currentLang}`);
  }

  /**
   * Translate key
   */
  t(key, params = {}) {
    const translation = translations[this.currentLang]?.[key] || 
                       translations[this.fallbackLang]?.[key] || 
                       key;
    
    // Replace parameters
    return translation.replace(/\{(\w+)\}/g, (match, p1) => params[p1] || match);
  }

  /**
   * Set language
   */
  setLanguage(lang) {
    if (!translations[lang]) {
      console.warn(`Language ${lang} not supported, falling back to ${this.fallbackLang}`);
      lang = this.fallbackLang;
    }
    
    if (this.currentLang === lang) return;
    
    this.currentLang = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    
    this.updatePageContent();
    this.notifyListeners();
    
    console.log(`üåê Language changed to: ${lang}`);
    
    // Dispatch event
    window.dispatchEvent(new CustomEvent('languageChanged', {
      detail: { language: lang }
    }));
  }

  /**
   * Toggle language
   */
  toggleLanguage() {
    const newLang = this.currentLang === 'en' ? 'hi' : 'en';
    this.setLanguage(newLang);
    return newLang;
  }

  /**
   * Update all page content
   */
  updatePageContent() {
    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = this.t(key);
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.t(key);
    });
    
    // Update titles
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      el.title = this.t(key);
    });
    
    // Update alt text
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
      const key = el.getAttribute('data-i18n-alt');
      el.alt = this.t(key);
    });
    
    // Update value attributes (for buttons, inputs)
    document.querySelectorAll('[data-i18n-value]').forEach(el => {
      const key = el.getAttribute('data-i18n-value');
      el.value = this.t(key);
    });
    
    // Update aria-labels
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      el.setAttribute('aria-label', this.t(key));
    });

    // Update language toggle button
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
      langToggle.textContent = this.currentLang === 'en' ? 'HI' : 'EN';
    }

    // Update HTML direction (for RTL languages if needed)
    document.documentElement.dir = 'ltr'; // Hindi uses LTR
  }

  /**
   * Add translation listener
   */
  subscribe(listener) {
    this.listeners.push(listener);
    listener(this.currentLang);
    
    // Return unsubscribe function
    return () => {
      this.listeners = this.listeners.filter(l => l !== listener);
    };
  }

  /**
   * Notify all listeners
   */
  notifyListeners() {
    this.listeners.forEach(listener => listener(this.currentLang));
  }

  /**
   * Get current language
   */
  getCurrentLang() {
    return this.currentLang;
  }

  /**
   * Check if language is RTL
   */
  isRTL() {
    return false; // Hindi and English are LTR
  }

  /**
   * Get direction
   */
  getDir() {
    return 'ltr';
  }

  /**
   * Format number according to locale
   */
  formatNumber(number, options = {}) {
    return new Intl.NumberFormat(this.currentLang === 'hi' ? 'hi-IN' : 'en-IN', options).format(number);
  }

  /**
   * Format date according to locale
   */
  formatDate(date, options = {}) {
    return new Intl.DateTimeFormat(this.currentLang === 'hi' ? 'hi-IN' : 'en-IN', options).format(date);
  }

  /**
   * Format currency
   */
  formatCurrency(amount) {
    return new Intl.NumberFormat(this.currentLang === 'hi' ? 'hi-IN' : 'en-IN', {
      style: 'currency',
      currency: 'INR',
      minimumFractionDigits: 0,
      maximumFractionDigits: 0
    }).format(amount);
  }

  /**
   * Get all available languages
   */
  getAvailableLanguages() {
    return Object.keys(translations).map(code => ({
      code,
      name: code === 'en' ? 'English' : '‡§π‡§ø‡§®‡•ç‡§¶‡•Ä'
    }));
  }

  /**
   * Check if key exists
   */
  hasKey(key) {
    return !!(translations[this.currentLang]?.[key] || translations[this.fallbackLang]?.[key]);
  }

  /**
   * Add new translation (for dynamic content)
   */
  addTranslation(key, value, lang = this.currentLang) {
    if (!translations[lang]) {
      translations[lang] = {};
    }
    translations[lang][key] = value;
  }

  /**
   * Clean up
   */
  destroy() {
    this.listeners = [];
    console.log('üßπ I18n destroyed');
  }
}

